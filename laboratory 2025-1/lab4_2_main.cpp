/*
I.	Разработать функции для выполнения операций над текстовым файлом  С++:
•	создание текстового файла, содержащего числовые значения по одному на строку;
•	вывод содержимого текстового файла;
•	добавление новой записи в конец файла:
•	разработать функции для дополнительных операций, указанных в варианте
II.	Разработать приложение и выполнить тестирование всех функций.

Добавить в файл новую строку перед первой строкой. Путем создания нового файла.
 */

#include "lab4_2_class.h"

int main() {
    SetConsoleCP(1251);
    SetConsoleOutputCP(1251);
    setlocale(LC_ALL, "Russian");

    // Имя файла
    string fileName = "lab42.txt";

    // Тестовые числовые значения
    vector<int> values = {10, 20, 30, 50};

    // Создаем менеджер для работы с файлом
    Text_menu manager(fileName);

    // Создаем файл
    manager.createFile(values);

    // Выводим содержимое файла
    cout << "Содержание файла после создания:" << endl;
    manager.printFile();

    // Добавляем новую запись в конец файла
    manager.addToFile(100);

    // Выводим обновленное содержимое файла
    cout << "\nСодержание файла после добавления новой записи:" << endl;
    manager.printFile();

    // Вставляем строку перед первой строкой
    string newLine = "Новая строка перед первой";
    manager.addToStartFile(newLine);

    // Выводим итоговое содержимое файла
    cout << "\nСодержание файла после вставки строки перед первой строкой:" << endl;
    manager.printFile();

    return 0;
}